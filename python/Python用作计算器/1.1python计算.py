print(2  +  2) # 打印2 + 2的值
print(2 - 2) # 打印2 - 2的值
print(2 * 3) # 打印2 * 2的值
print(16 / 2) # 打印16 / 2的值，除法总是返回一个浮点数
print(-13 // 2) # 得到一个整数结果，向下取整
print(5 ** 2) # 打印5的二次方的值
width = 20
height = 5 * 9
print(width * height) # 20赋值给width，5*9的结果赋值给height，并使用print（）函数输出结果。
